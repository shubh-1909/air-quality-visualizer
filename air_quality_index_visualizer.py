# Sub-index calculation based on CPCB breakpoints
def calculate_sub_index(conc, breakpoints):
    for bp in breakpoints:
        if bp["low"] <= conc <= bp["high"]:
            Clow = bp["low"]
            Chigh = bp["high"]
            Ilow = bp["index_low"]
            Ihigh = bp["index_high"]
            return round(((Ihigh - Ilow) / (Chigh - Clow)) * (conc - Clow) + Ilow)
    return None

